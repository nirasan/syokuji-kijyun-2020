package main

import (
	"flag"
	"fmt"
	"strings"

	"github.com/nirasan/syokuji-kijyun-2020/pkg"
	"github.com/nirasan/syokuji-kijyun-2020/tools"
)

func main() {
	flag.Parse()
	pkgName := flag.Arg(0)
	outputFile := flag.Arg(1)

	data := make([]pkg.CarbohydratesDatum, 0)

	lines := strings.Split(src, "\n")
	for i := 0; i <= 14; i++ {
		cols := strings.Split(lines[i], " ")
		fmt.Printf("cols:%+v, len:%d", cols, len(cols))
		if len(cols) < 2 {
			continue
		}
		j := len(cols) - 2
		c1 := newCarbohydrates(pkg.GenderMale, i, cols[j:j+1])
		c2 := newCarbohydrates(pkg.GenderFemale, i, cols[j+1:j+2])
		data = append(data, c1, c2)
	}

	g := tools.NewGenerator()
	g.MustWrite("// Code generated by \"bin/carbohydrates/main.go\"; DO NOT EDIT.\n")
	g.MustWrite("package %s\n", pkgName)
	g.MustWrite("func (c *Carbohydrates) Data() []CarbohydratesDatum {\n")
	g.MustWrite("    return []CarbohydratesDatum {\n")
	for _, d := range data {
		s := fmt.Sprintf("%#v,\n", d)
		s = strings.ReplaceAll(s, "pkg.CarbohydratesDatum", "CarbohydratesDatum")
		s = strings.ReplaceAll(s, "pkg.NullFloat64", "NullFloat64")
		g.MustWrite(s)
	}
	g.MustWrite("    }\n")
	g.MustWrite("}\n")

	g.Generate(outputFile)
}

func newCarbohydrates(gender, age int, cols []string) pkg.CarbohydratesDatum {
	c := pkg.CarbohydratesDatum{
		Gender: gender,
		Age:    age,
	}
	DGs := strings.Split(cols[0], "～")
	if len(DGs) == 2 {
		c.DGMin = pkg.NullFloat64FromString(DGs[0])
		c.DGMax = pkg.NullFloat64FromString(DGs[1])
	} else {
		c.DGMin = pkg.NewNullFloat64(nil)
		c.DGMax = pkg.NewNullFloat64(nil)
	}
	return c
}

