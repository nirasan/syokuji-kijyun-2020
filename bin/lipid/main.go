package main

import (
	"flag"
	"fmt"
	"strings"

	"github.com/nirasan/syokuji-kijyun-2020/pkg"
	"github.com/nirasan/syokuji-kijyun-2020/tools"
)

func main() {
	flag.Parse()
	pkgName := flag.Arg(0)
	outputFile := flag.Arg(1)

	data := make([]pkg.LipidDatum, 0)

	lines := strings.Split(src, "\n")
	for i := 0; i <= 14; i++ {
		cols := strings.Split(lines[i], " ")
		fmt.Printf("cols:%+v, len:%d", cols, len(cols))
		if len(cols) < 4 {
			continue
		}
		j := len(cols) - 4
		l1 := newLipid(pkg.GenderMale, i, cols[j:j+2])
		l2 := newLipid(pkg.GenderFemale, i, cols[j+2:j+4])
		data = append(data, l1, l2)
	}

	g := tools.NewGenerator()
	g.MustWrite("// Code generated by \"bin/lipid/main.go\"; DO NOT EDIT.\n")
	g.MustWrite("package %s\n", pkgName)
	g.MustWrite("func (l *Lipid) Data() []LipidDatum {\n")
	g.MustWrite("    return []LipidDatum {\n")
	for _, d := range data {
		s := fmt.Sprintf("%#v,\n", d)
		s = strings.ReplaceAll(s, "pkg.LipidDatum", "LipidDatum")
		s = strings.ReplaceAll(s, "pkg.NullFloat64", "NullFloat64")
		g.MustWrite(s)
	}
	g.MustWrite("    }\n")
	g.MustWrite("}\n")

	g.Generate(outputFile)
}

func newLipid(gender, age int, cols []string) pkg.LipidDatum {
	l := pkg.LipidDatum{
		Gender: gender,
		Age:    age,
		AI:     pkg.NullFloat64FromString(cols[0]),
	}
	DGs := strings.Split(cols[1], "～")
	if len(DGs) == 2 {
		l.DGMin = pkg.NullFloat64FromString(DGs[0])
		l.DGMax = pkg.NullFloat64FromString(DGs[1])
	} else {
		l.DGMin = pkg.NewNullFloat64(nil)
		l.DGMax = pkg.NewNullFloat64(nil)
	}
	return l
}

