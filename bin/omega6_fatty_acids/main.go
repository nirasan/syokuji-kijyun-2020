package main

import (
	"flag"
	"fmt"
	"strings"

	"github.com/nirasan/syokuji-kijyun-2020/pkg"
	"github.com/nirasan/syokuji-kijyun-2020/tools"
)

func main() {
	flag.Parse()
	pkgName := flag.Arg(0)
	outputFile := flag.Arg(1)

	data := make([]pkg.Omega6FattyAcidsDatum, 0)

	lines := strings.Split(src, "\n")
	for i := 0; i <= 14; i++ {
		cols := strings.Split(lines[i], " ")
		fmt.Printf("cols:%+v, len:%d", cols, len(cols))
		if len(cols) < 2 {
			continue
		}
		j := len(cols) - 2
		l1 := newOmega6FattyAcids(pkg.GenderMale, i, cols[j:j+1])
		l2 := newOmega6FattyAcids(pkg.GenderFemale, i, cols[j+1:j+2])
		data = append(data, l1, l2)
	}

	g := tools.NewGenerator()
	g.MustWrite("// Code generated by \"bin/omega6_fatty_acids/main.go\"; DO NOT EDIT.\n")
	g.MustWrite("package %s\n", pkgName)
	g.MustWrite("func (o *Omega6FattyAcids) Data() []Omega6FattyAcidsDatum {\n")
	g.MustWrite("    return []Omega6FattyAcidsDatum {\n")
	for _, d := range data {
		s := fmt.Sprintf("%#v,\n", d)
		s = strings.ReplaceAll(s, "pkg.Omega6FattyAcidsDatum", "Omega6FattyAcidsDatum")
		s = strings.ReplaceAll(s, "pkg.NullFloat64", "NullFloat64")
		g.MustWrite(s)
	}
	g.MustWrite("    }\n")
	g.MustWrite("}\n")

	g.Generate(outputFile)
}

func newOmega6FattyAcids(gender, age int, cols []string) pkg.Omega6FattyAcidsDatum {
	o := pkg.Omega6FattyAcidsDatum{
		Gender: gender,
		Age:    age,
		AI:     pkg.NullFloat64FromString(cols[0]),
	}
	return o
}

