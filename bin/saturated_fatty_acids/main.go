package main

import (
	"flag"
	"fmt"
	"strings"

	"github.com/nirasan/syokuji-kijyun-2020/pkg"
	"github.com/nirasan/syokuji-kijyun-2020/tools"
)

func main() {
	flag.Parse()
	pkgName := flag.Arg(0)
	outputFile := flag.Arg(1)

	data := make([]pkg.SaturatedFattyAcidsDatum, 0)

	for i := 0; i < 4; i++ {
		data = append(data, newNullSaturatedFattyAcids(pkg.GenderMale, i))
		data = append(data, newNullSaturatedFattyAcids(pkg.GenderFemale, i))
	}

	lines := strings.Split(src, "\n")
	for i := 4; i <= 14; i++ {
		cols := strings.Split(lines[i], " ")
		fmt.Printf("cols:%+v, len:%d", cols, len(cols))
		if len(cols) < 4 {
			continue
		}
		j := len(cols) - 4
		s1 := newSaturatedFattyAcids(pkg.GenderMale, i, cols[j:j+2])
		s2 := newSaturatedFattyAcids(pkg.GenderFemale, i, cols[j+2:j+4])
		data = append(data, s1, s2)
	}

	g := tools.NewGenerator()
	g.MustWrite("// Code generated by \"bin/saturated_fatty_acids/main.go\"; DO NOT EDIT.\n")
	g.MustWrite("package %s\n", pkgName)
	g.MustWrite("func (s *SaturatedFattyAcids) Data() []SaturatedFattyAcidsDatum {\n")
	g.MustWrite("    return []SaturatedFattyAcidsDatum {\n")
	for _, d := range data {
		s := fmt.Sprintf("%#v,\n", d)
		s = strings.ReplaceAll(s, "pkg.SaturatedFattyAcidsDatum", "SaturatedFattyAcidsDatum")
		s = strings.ReplaceAll(s, "pkg.NullFloat64", "NullFloat64")
		g.MustWrite(s)
	}
	g.MustWrite("    }\n")
	g.MustWrite("}\n")

	g.Generate(outputFile)
}

func newNullSaturatedFattyAcids(gender, age int) pkg.SaturatedFattyAcidsDatum {
	return pkg.SaturatedFattyAcidsDatum{
		Gender: gender,
		Age:    age,
		DGMax:  pkg.NullFloat64{},
	}
}

func newSaturatedFattyAcids(gender, age int, cols []string) pkg.SaturatedFattyAcidsDatum {
	s := pkg.SaturatedFattyAcidsDatum{
		Gender: gender,
		Age:    age,
	}
	s.DGMax = pkg.NullFloat64FromString(cols[0])
	return s
}

