package main

import (
	"flag"
	"fmt"
	"strings"

	"github.com/nirasan/syokuji-kijyun-2020/pkg"
	"github.com/nirasan/syokuji-kijyun-2020/tools"
)

func main() {
	flag.Parse()
	pkgName := flag.Arg(0)
	outputFile := flag.Arg(1)

	data := make([]pkg.VitaminDDatum, 0)

	lines := strings.Split(src, "\n")
	for i := 0; i <= 14; i++ {
		line := strings.ReplaceAll(lines[i], ",", "")
		cols := strings.Split(line, " ")
		j := len(cols) - 4
		d1 := newVitaminD(pkg.GenderMale, i, cols[j:j+2])
		d2 := newVitaminD(pkg.GenderFemale, i, cols[j+2:j+4])
		data = append(data, d1, d2)
	}

	g := tools.NewGenerator()
	g.MustWrite("// Code generated by \"bin/vitamin_a/main.go\"; DO NOT EDIT.\n")
	g.MustWrite("package %s\n", pkgName)
	g.MustWrite("func (vit *VitaminD) Data() []VitaminDDatum {\n")
	g.MustWrite("    return []VitaminDDatum {\n")
	for _, d := range data {
		s := fmt.Sprintf("%#v,\n", d)
		s = strings.ReplaceAll(s, "pkg.VitaminDDatum", "VitaminDDatum")
		s = strings.ReplaceAll(s, "pkg.NullFloat64", "NullFloat64")
		g.MustWrite(s)
	}
	g.MustWrite("    }\n")
	g.MustWrite("}\n")

	g.Generate(outputFile)
}

func newVitaminD(gender, age int, cols []string) pkg.VitaminDDatum {
	vit := pkg.VitaminDDatum{
		Gender: gender,
		Age:    age,
		AI:     pkg.NullFloat64FromString(cols[0]),
		UL:     pkg.NullFloat64FromString(cols[1]),
	}
	return vit
}

