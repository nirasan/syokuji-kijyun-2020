package main

import (
	"flag"
	"fmt"
	"strings"

	"github.com/nirasan/syokuji-kijyun-2020/pkg"
	"github.com/nirasan/syokuji-kijyun-2020/tools"
)

func main() {
	flag.Parse()
	pkgName := flag.Arg(0)
	outputFile := flag.Arg(1)

	data := make([]pkg.VitaminKDatum, 0)

	lines := strings.Split(src, "\n")
	for i := 0; i <= 14; i++ {
		line := strings.ReplaceAll(lines[i], ",", "")
		cols := strings.Split(line, " ")
		j := len(cols) - 2
		d1 := newVitaminK(pkg.GenderMale, i, cols[j])
		d2 := newVitaminK(pkg.GenderFemale, i, cols[j+1])
		data = append(data, d1, d2)
	}

	g := tools.NewGenerator()
	g.MustWrite("// Code generated by \"bin/vitamin_a/main.go\"; DO NOT EDIT.\n")
	g.MustWrite("package %s\n", pkgName)
	g.MustWrite("func (vit *VitaminK) Data() []VitaminKDatum {\n")
	g.MustWrite("    return []VitaminKDatum {\n")
	for _, d := range data {
		s := fmt.Sprintf("%#v,\n", d)
		s = strings.ReplaceAll(s, "pkg.VitaminKDatum", "VitaminKDatum")
		s = strings.ReplaceAll(s, "pkg.NullFloat64", "NullFloat64")
		g.MustWrite(s)
	}
	g.MustWrite("    }\n")
	g.MustWrite("}\n")

	g.Generate(outputFile)
}

func newVitaminK(gender, age int, col string) pkg.VitaminKDatum {
	vit := pkg.VitaminKDatum{
		Gender: gender,
		Age:    age,
		AI:     pkg.NullFloat64FromString(col),
	}
	return vit
}

